#we use sep arguments in python
val1,val2,val3,val4=100,200,300,400

#printing variables whithout seprator
print(val1,val2,val3,val4)

#printing using comma
print(val1,val2,val3,val4,sep=',')

#printin using space
print(val1,val2,val3,val4,sep=' ')

#printing using colon as saprator
print(val1,val2,val3,val4,sep=':')

#printing using ------ as saprator
print(val1,val2,val3,val4,sep='----')