#assinment oprators
num1=int(input("Enter first number: "))
num2=int(input("enter second number: "))

#using num1+=num2 this will works as num1=num1+num2
re=num2
re+=num1
print("num1+=num2 =",re)

#using num1-=num2 this will work as num1=num1-num2
re=num2
re-=num1
print("num1-=num2 =",re)

#using num1/=num2 this will work as num1=num1/num2
re=num2
re/=num1
print("num1/=num2 =",re)

#using num1//=num2 this will work as num1=num1//num2
re=num2
re//=num1
print("num1//=num2 =",re)

#using num1**=num2 this will work as num1=num1**num2
re=num2
re**=num1
print("num1**=num2 =",re)

#using num1%=num2 this will work as num1=num1%num2
re=num2
re%=num1
print("num1%=num2 =",re)
